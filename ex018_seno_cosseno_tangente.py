import math
angulo = float(input('Digite um 창ngulo: '))
print(f'O 창ngulo de {angulo} tem o SENO de {math.sin(math.radians(angulo)):.2f}')
print(f'O 창ngulo de {angulo} tem o COSSENO de {math.cos(math.radians(angulo)):.2f}')
print(f'O 창ngulo de {angulo} tem a TANGENTE de {math.tan(math.radians(angulo)):.2f}')